#!/usr/bin/env bash
# dev/swtpm_setup.sh — start/stop/reset a software TPM for laptop development.
#
# Constraint 1 (CLAUDE.md): all TPM code is developed against swtpm via
# TPM2TOOLS_TCTI / TSS2_TCTI, then runs unchanged on the Pi's real TPM.
#
# Usage:
#   dev/swtpm_setup.sh start    # start swtpm (idempotent), write dev/tcti.env
#   dev/swtpm_setup.sh stop     # stop swtpm
#   dev/swtpm_setup.sh reset    # stop, wipe state, start fresh (all PCRs zero)
#   dev/swtpm_setup.sh status   # is it running?
#
# After 'start' or 'reset':
#   source dev/tcti.env
#   tpm2_pcrread sha256:10

set -euo pipefail

DEV_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
STATE_DIR="${SWTPM_STATE_DIR:-$DEV_DIR/swtpm-state}"
PID_FILE="$STATE_DIR/swtpm.pid"
SERVER_PORT="${SWTPM_SERVER_PORT:-2321}"
CTRL_PORT="${SWTPM_CTRL_PORT:-2322}"
TCTI="swtpm:host=127.0.0.1,port=$SERVER_PORT"
ENV_FILE="$DEV_DIR/tcti.env"

is_running() {
    [[ -f "$PID_FILE" ]] && kill -0 "$(cat "$PID_FILE")" 2>/dev/null
}

write_env_file() {
    cat > "$ENV_FILE" <<EOF
# Generated by dev/swtpm_setup.sh — 'source dev/tcti.env' to talk to the software TPM.
export TPM2TOOLS_TCTI="$TCTI"
export TSS2_TCTI="$TCTI"
EOF
}

start() {
    if is_running; then
        echo "swtpm already running (pid $(cat "$PID_FILE"))"
    else
        mkdir -p "$STATE_DIR"
        swtpm socket \
            --tpm2 \
            --tpmstate "dir=$STATE_DIR" \
            --server "type=tcp,port=$SERVER_PORT" \
            --ctrl "type=tcp,port=$CTRL_PORT" \
            --flags not-need-init,startup-clear \
            --pid "file=$PID_FILE" \
            --daemon
        echo "swtpm started (pid $(cat "$PID_FILE"), state: $STATE_DIR)"
    fi
    write_env_file
    cat <<EOF

To use it in your shell:
    source $ENV_FILE
    tpm2_pcrread sha256:10
EOF
}

stop() {
    if is_running; then
        kill "$(cat "$PID_FILE")"
        echo "swtpm stopped"
    else
        echo "swtpm not running"
    fi
    rm -f "$PID_FILE"
}

case "${1:-start}" in
    start)  start ;;
    stop)   stop ;;
    reset)  stop; rm -rf "$STATE_DIR"; start ;;
    status) if is_running; then echo "running (pid $(cat "$PID_FILE"))"; else echo "not running"; exit 1; fi ;;
    *) echo "usage: $0 {start|stop|reset|status}" >&2; exit 2 ;;
esac
